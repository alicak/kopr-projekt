    public OknoKlienta() {
        initComponents();

        File info = new File("infooprerusenom.txt");
        if (info.exists()) { // je mozne pokracovat v stahovani suboru
            Scanner citac = null;
            try {
                citac = new Scanner(info);
                String nazov = citac.next();
                int pocetSoketov = citac.nextInt();
                long pocetPrijatychBajtov = citac.nextLong();
                List<Long> offsety = new ArrayList<>();
                for (int i = 0; i < pocetSoketov; i++) {
                    offsety.add(citac.nextLong());
                }

                infoNaPokracovanie = new InfoNaPokracovanie(nazov, pocetSoketov, pocetPrijatychBajtov, offsety);

                lblPrerusenyNazov.setText(nazov);
                lblPrerusenyNazov.setEnabled(true);
                btnObnovit.setEnabled(true);

            } catch (FileNotFoundException ex) {
                System.err.println("Nepodarilo sa otvorit subor s informaciami o pokracovani.");
            } finally {
                if (citac != null) {
                    // po precitani subor vymazeme
                    citac.close();
                    info.delete();
                }
            }
        }
    }

    private void spustiProgresBarWorker() {
        progresBarWorker = new ProgresBarWorker(this, klient);
        progresBarWorker.execute();
    }

    private void spustiKopirovaciWorker() {
        kopirovaciWorker = new KopirovaciWorker(this, klient);
        kopirovaciWorker.execute();
    }

    public void zastavProgresBarWorker() {
        progresBarWorker.cancel(true);
    }

