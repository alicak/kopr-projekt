    private void btnZacatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnZacatActionPerformed
        String nazovSuboru = txtNoveNazov.getText();
        String pocetSoketovTxt = txtPocetSoketov.getText();

        if (nazovSuboru.isEmpty() || pocetSoketovTxt.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Nesprávne vyplnené polia!",
                    "Chyba",
                    ERROR_MESSAGE);
            return;
        }

        File subor = new File(nazovSuboru);
        if (subor.exists()) {
            JOptionPane.showMessageDialog(this,
                    "Súbor s daným názvom už existuje.",
                    "Chyba",
                    ERROR_MESSAGE);
            return;
        }

        int pocetSoketov = Integer.parseInt(pocetSoketovTxt);

        klient = new Klient(nazovSuboru, pocetSoketov);

        spustiKopirovaciWorker();
        spustiProgresBarWorker();
        
        btnZacat.setEnabled(false);
        btnObnovit.setEnabled(false);
        btnPrerusit.setEnabled(true);
        btnUkoncit.setEnabled(true);
    }//GEN-LAST:event_btnZacatActionPerformed

    private void btnPrerusitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrerusitActionPerformed
        klient.prerusStahovanie(true);
        JOptionPane.showMessageDialog(this,
                "Sťahovanie bolo prerušené. Program sa ukončí.",
                "Info",
                INFORMATION_MESSAGE);
        while (!kopirovaciWorker.isDone()) {
            // cakame, kym worker urobi operacie po skonceni kopirovania
            continue;
        }
        System.exit(0);
    }//GEN-LAST:event_btnPrerusitActionPerformed

